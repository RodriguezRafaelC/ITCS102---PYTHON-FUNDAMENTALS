n1 = eval(input("Enter a Number : "))
n2 = eval(input("Enter a Number : "))

plus = n1 + n2
minus = n1 - n2
multiply = n1 * n2
divide = n1 / n2
exponent = n1 ** n2
floor = n1 // n2

print(n1, "+", n2, "=", plus)
print(n1, "-", n2, "=", minus)
print(n1, "*", n2, "=", multiply)
print(n1, "/", n2, "=", divide)
print(n1, "**", n2, "=", exponent)
print(n1, "//", n2, "=", floor)